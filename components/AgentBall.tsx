export function AgentBall({ className = "h-4 w-4", active = false }: AgentBallProps) {
  return (
    <svg
      className={`${className} pointer-events-none flex-shrink-0 ${active ? "dg-agent-ball-active" : ""}`}
      viewBox="0 0 22 22"
      aria-label="Agent Ball"
      role="img"
    >
      {ballCircles.map((circle, index) => (
        <circle key={`${circle.cx}-${circle.cy}-${index}`} {...circle} />
      ))}
    </svg>
  );
}

export function AgentThinkingIndicator({ label = "Dreaming...", className = "", hideBall = false }: AgentThinkingIndicatorProps) {
  const chars = Array.from(label);
  const step = chars.length > 0 ? 4 / chars.length : 0;

  return (
    <div className={`flex min-w-0 items-center ${className}`}>
      {!hideBall && <AgentBall className="mr-2 h-4 w-4" active />}
      <span role="img" aria-label={label} className="truncate pt-0.5 text-xs italic text-current transition-opacity duration-500 ease-in-out">
        {chars.map((char, index) => {
          const style: CSSProperties = {
            animation: "dg-shimmer-char 4s linear infinite",
            animationDelay: `${-index * step}s`,
          };

          return (
            <span key={`${char}-${index}`} className="inline-block dg-shimmer-char" style={style}>
              {char === " " ? "\u00a0" : char}
            </span>
          );
        })}
      </span>
    </div>
  );
}
